// ─── PDF Generator ────────────────────────────────────────────────────────────

function generateBillPDF(bill: Bill) {
  const doc = new jsPDF() as any;

  // Title
  doc.setFillColor(30, 41, 59);
  doc.rect(0, 0, 210, 35, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(20);
  doc.setFont('helvetica', 'bold');
  doc.text('PURCHASE BILL', 105, 16, { align: 'center' });
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  doc.text('Farmers Factory — FF Operations', 105, 25, { align: 'center' });

  doc.setTextColor(30, 41, 59);

  // Bill info
  doc.setFontSize(10);
  doc.setFont('helvetica', 'bold');
  doc.text(`Bill No: ${bill.billNumber}`, 14, 45);
  doc.text(`Date: ${bill.billDate}`, 140, 45);
  doc.setFont('helvetica', 'normal');
  doc.text(`Due Date: ${bill.dueDate || '—'}`, 140, 52);
  if (bill.poReference) doc.text(`PO Ref: ${bill.poReference}`, 140, 59);

  // Vendor
  doc.setFont('helvetica', 'bold');
  doc.text('Vendor:', 14, 55);
  doc.setFont('helvetica', 'normal');
  doc.text(bill.vendor.name, 14, 62);
  doc.text(bill.vendor.phone, 14, 68);
  doc.text(bill.vendor.address, 14, 74);
  doc.text(`GSTIN: ${bill.vendor.gstin}`, 14, 80);

  // Bank
  doc.setFont('helvetica', 'bold');
  doc.text('Bank Details:', 14, 92);
  doc.setFont('helvetica', 'normal');
  doc.text(`${bill.bank.bankName}`, 14, 99);
  doc.text(`A/C: ${bill.bank.accountNumber}`, 14, 105);
  doc.text(`IFSC: ${bill.bank.ifscCode}`, 14, 111);
  doc.text(`Branch: ${bill.bank.branch}`, 14, 117);

  // Items table
  autoTable(doc, {
    startY: 127,
    head: [['Product', 'Qty', 'Unit', 'Rate (₹)', 'Disc%', 'Tax%', 'Amount (₹)']],
    body: bill.lineItems.map(i => [
      i.product, i.qty, i.unit,
      `₹${i.rate.toLocaleString('en-IN')}`,
      `${i.discount}%`, `${i.tax}%`,
      `₹${i.amount.toLocaleString('en-IN', { minimumFractionDigits: 2 })}`,
    ]),
    headStyles: { fillColor: [30, 41, 59], fontSize: 9 },
    bodyStyles: { fontSize: 9 },
    foot: [['', '', '', '', '', 'SUB TOTAL', `₹${bill.subTotal.toLocaleString('en-IN', { minimumFractionDigits: 2 })}`]],
    footStyles: { fillColor: [243, 244, 246], textColor: [30, 41, 59], fontStyle: 'bold', fontSize: 9 },
  });

  const finalY = (doc as any).lastAutoTable.finalY + 8;

  // Totals
  const totalsData = [
    ['CGST (2.5%)', `+₹${bill.cgst.toFixed(2)}`],
    ['SGST (2.5%)', `+₹${bill.sgst.toFixed(2)}`],
    ['TOTAL AMOUNT', `₹${bill.total.toFixed(2)}`],
  ];

  let y = finalY;
  doc.setFontSize(10);
  totalsData.forEach(([label, value], idx) => {
    const isFinal = idx === totalsData.length - 1;
    if (isFinal) {
      doc.setFillColor(34, 197, 94);
      doc.rect(120, y - 5, 76, 10, 'F');
      doc.setTextColor(255, 255, 255);
      doc.setFont('helvetica', 'bold');
    } else {
      doc.setTextColor(80, 80, 80);
      doc.setFont('helvetica', 'normal');
    }
    doc.text(label, 125, y);
    doc.text(value, 192, y, { align: 'right' });
    y += 10;
  });

  if (bill.notes) {
    doc.setTextColor(80, 80, 80);
    doc.setFont('helvetica', 'italic');
    doc.setFontSize(9);
    doc.text(`Notes: ${bill.notes}`, 14, y + 5);
  }

  doc.save(`${bill.billNumber}.pdf`);
  toast.success(`📄 ${bill.billNumber}.pdf downloaded`);
}

