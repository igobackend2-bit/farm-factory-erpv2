function loadBills(): Bill[] {
  try {
    const raw = localStorage.getItem(BILL_STORE_KEY);
    if (!raw) {
      localStorage.setItem(BILL_STORE_KEY, JSON.stringify(DEMO_BILLS));
      return [...DEMO_BILLS];
    }
    return JSON.parse(raw) as Bill[];
  } catch { return [...DEMO_BILLS]; }
}

function saveBills(bills: Bill[]): void {
  localStorage.setItem(BILL_STORE_KEY, JSON.stringify(bills));
}

function getNextBillSerial(): string {
  const bills = loadBills();
  const max = bills.reduce((m, b) => {
    const n = parseInt(b.billNumber.replace('BILL-', ''), 10) || 0;
    return Math.max(m, n);
  }, 0);
  return `BILL-${String(max + 1).padStart(3, '0')}`;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function fmt(n: number) { return n.toLocaleString('en-IN', { minimumFractionDigits: 2, maximumFractionDigits: 2 }); }

function calcBillTotals(items: BillLineItem[]) {
  const subTotal = items.reduce((s, i) => s + i.qty * i.rate, 0);
  const discountAmount = items.reduce((s, i) => s + (i.qty * i.rate * i.discount / 100), 0);
  const taxableAmount = subTotal - discountAmount;
  const totalTax = items.reduce((s, i) => s + (i.qty * i.rate * (1 - i.discount / 100) * i.tax / 100), 0);
  const cgst = totalTax / 2;
  const sgst = totalTax / 2;
  const total = taxableAmount + totalTax;
  return { subTotal, discountAmount, taxableAmount, cgst, sgst, total };
}

